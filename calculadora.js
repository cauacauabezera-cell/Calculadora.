let x = 10, y = 2;

// Adição
let soma = x + y;
console.log(`A soma de ${x} e ${y} é ${soma}`);

// Subtração
let subtracao = x - y;
console.log(`A subtração de ${x} e ${y} é ${subtracao}`);

// Multiplicação
let multiplicacao = x * y;
console.log(`A multiplicação de ${x} e ${y} é ${multiplicacao}`);

// Divisão
let divisao = x / y;
console.log(`A divisão de ${x} e ${y} é ${divisao}`);